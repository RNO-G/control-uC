/*!
 * Prints the provided buffer in HEX
 * 
 * \param buffer Buffer to be printed
 * \param size   Buffer size to be printed
 */
void PrintHexBuffer( uint8_t *buffer, uint8_t size )
{
    uint8_t newline = 0;

    for( uint8_t i = 0; i < size; i++ ) { 
      if( newline != 0 ) { 
        printf( "\r\n" );
        newline = 0;
      }
      
      printf( "%02X ", buffer[i] );

      if( ( ( i + 1 ) % 16 ) == 0 ) {
        newline = 1;
      }
    }
    printf( "\r\n" );
}


